import { readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const rootDir = join(__dirname, "..");
const catalogPath = join(rootDir, "catalog-data.js");
const outputPath = join(rootDir, "catalog-meta.js");
const sourceUrl = "https://raw.githubusercontent.com/ByMykel/CSGO-API/main/public/api/en/skins.json";

function extractCatalogItems(fileContent) {
  const marker = "globalThis.CS2_CATALOG = ";
  const start = fileContent.indexOf(marker);
  if (start === -1) throw new Error("Unable to parse CS2_CATALOG from catalog-data.js");

  const afterMarker = fileContent.slice(start + marker.length);
  const end = afterMarker.indexOf(";\n");
  return JSON.parse((end === -1 ? afterMarker : afterMarker.slice(0, end)).trim());
}

async function main() {
  const catalogContent = await readFile(catalogPath, "utf8");
  const localItems = extractCatalogItems(catalogContent);

  const response = await fetch(sourceUrl, {
    headers: { "User-Agent": "CS2-Relic-Hall" },
    signal: AbortSignal.timeout(30000)
  });
  if (!response.ok) {
    throw new Error(`Metadata request failed: ${response.status}`);
  }

  const upstreamItems = await response.json();
  const upstreamById = new Map(upstreamItems.map((item) => [item.id, item]));
  const meta = {};

  for (const item of localItems) {
    const upstream = upstreamById.get(item.id);
    if (!upstream) continue;

    meta[item.id] = {
      englishName: upstream.name || null,
      phase: upstream.phase || null,
      paintIndex: upstream.paint_index || null,
      patternId: upstream.pattern?.id || null,
      specialType: upstream.phase ? "phase" : (/Case Hardened/i.test(upstream.name || "") ? "case-hardened" : null)
    };
  }

  const output = `// Generated by scripts/build-catalog-meta.mjs. Do not edit manually.\n` +
    `globalThis.CS2_ITEM_META = ${JSON.stringify(meta)};\n`;

  await writeFile(outputPath, output, "utf8");
  console.log(`Wrote ${Object.keys(meta).length} metadata records to ${outputPath}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
