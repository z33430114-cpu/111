import { readFile, writeFile } from "node:fs/promises";
import { join } from "node:path";
import vm from "node:vm";

const root = process.cwd();
const catalogSource = await readFile(join(root, "catalog-data.js"), "utf8");

const context = { globalThis: {} };
vm.createContext(context);
vm.runInContext(catalogSource, context, { filename: "catalog-data.js" });

const catalog = Array.isArray(context.globalThis.CS2_CATALOG) ? context.globalThis.CS2_CATALOG : [];
const stickers = catalog
  .filter((item) => item?.type === "sticker" && item?.id && item?.image)
  .map((item) => ({
    id: item.id,
    name: item.name || "",
    nameEn: item.nameEn || "",
    nameZh: item.nameZh || "",
    collection: item.collection || "",
    collectionEn: item.collectionEn || "",
    collectionZh: item.collectionZh || "",
    image: item.image || ""
  }));

const output = `// Generated by scripts/build-stickers-data.mjs. Do not edit manually.\n` +
  `globalThis.CS2_STICKERS = ${JSON.stringify(stickers)};\n`;

await writeFile(join(root, "stickers-data.js"), output, "utf8");
console.log(`Generated stickers-data.js with ${stickers.length} stickers.`);
